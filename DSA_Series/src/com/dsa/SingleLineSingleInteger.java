package com.dsa;

import java.util.Scanner;

public class SingleLineSingleInteger {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.println("Pass your input value you will found value+1 in your result");
		int n = sc.nextInt();
		System.out.println("Result is : 5");
		System.out.println(n + 1);
	}

}

// Input : 4
// Output : 5